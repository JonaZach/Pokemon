package com.plainid.assignment.dao;

public class Trainer {
    String name;
    int level;
    String[] bag;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String[] getBag() {
        return bag;
    }

    public void setBag(String[] bag) {
        this.bag = bag;
    }
}
